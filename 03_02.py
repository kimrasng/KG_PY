# 산술연산자에는 대입연산자(=, +=, -=,,,,등) 가 포함된다.
# 대입연산자

a = 10
a += 5;print(a) # a+=5 ==> a=a+5와 같다

a -= 5;print(a) # a-=5 ==> a=a-5와 같다

a *= 5;print(a) # a*=5 ==> a=a*5와 같다

a /= 5;print(a) # a/=5 ==> a=a/5와 같다

a //= 5;print(a) # a//=5 ==> a=a//5와 같다

a %= 5;print(a) # a%=5 ==> a=a%5와 같다

a **= 5;print(a) # a**=5 ==> a=a**5와 같다

# 대입 연산자 = 이외에도 +=, -=, *=, /=, %=, //=, **= tkdyd
# 예) 첫 번째 대입 연산자 a+=5은 a에 5를 더해서 다시a에 넣으라는 의미로 a=a+5와 같음

