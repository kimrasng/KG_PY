# 산술 연산자의 종류(+, -, *, /, %, //, **)
# 더하기, 빼기, 곱하기, 나누기, 나머지, 몫, 곱

s1, s2, s3 ="100","100.123", "99999"    # 사눗ㄹ 연산을 하는 문자열을 숫자로 변환

int(s1)+1   # 문자형 변수를 정수형 변수(int) 로 변환 후 + 1

float(s2)+1 # 문자형 변수를 실수형 변수(float) 로 변환 후 + 1

int(s3)+1   # 문자형 변수를 정수형 변수(int) 로 변환후 + 1

# 문자열이 숫자로 구성되어 있을 때, int() 또는 float() 함수 사용해서 정수나 실수로 변환
# 문자열 int() 함수가 정수로, float() 함수가 실수로 변경


# 숫자를 문자로 변환(str()는 문자로 변환 하는 함수)

